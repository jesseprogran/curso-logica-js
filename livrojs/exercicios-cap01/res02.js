/*
    Elabore um programa para uma pizzaria o qual leia o valor total de uma conta e quantos clientes vão pagala. calcule e informe o valor a ser pago por cliente.
*/

let valorTotal = Number(prompt("Qual o valor total"));
let cliente = Number(prompt("Número de clientes"))

let cliente1 = valorTotal;
let cliente2 = valorTotal;
let cliente3 = valorTotal;
let div = cliente
let total = cliente1 = cliente2 = cliente3; 
let result = total / div;

console.log(` O valor total da compra foi de R$${total.toFixed(2)} e os ${cliente} cliente vão pagar R$${result.toFixed(2)} cada um.`);